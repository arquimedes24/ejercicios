
package ejercicioif_else;
public class Ejercicioif_else {
    public static void main(String[] args) {
       
        int a,b;
        a=2;
        b=3;
        
        if(a > 2) {
            b++;
        } else {
            b--;
            System.out.println(b);
            System.out.println(a);
        }
        
    }
    
}
